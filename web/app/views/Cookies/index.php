			<h3>TIPOS DE COOKIES</h3>
			<br><br>
			<section>
				<h4>Según quien sea la entidad que las gestione</h4>
				<dl>
					<div>
						<dt>Propias</dt>: <dd>las genera nuestro sitio web.</dd>
					</div>
					<div>
						<dt>De terceros</dt>: <dd>son cookies que recibe el usuario al navegar en nuestra web pero que son generadas por un servicio perteneciente a otra entidad.</dd>
					</div>		
				</dl>
			</section>
			<section>
				<h4>Según el plazo de tiempo que permanecen activadas</h4>
				<dl>
					<div>
						<dt>Cookies de sesión</dt>: <dd>son cookies diseñadas para recabar y almacenar datos mientras el usuario está accediendo a una página web y no quedan registradas en su equipo.</dd>
						</dd>
					</div>
					<div>
						<dt>Cookies persistentes</dt>: <dd>son cookies en las que los datos siguen almacenados en el terminal y pueden ser accedidos y tratados durante un periodo definido por el responsable de la cookie, y que puede ir de unos minutos a varios años.
					</div>
				</dl>
			</section>
			<section>
				<h4>Según la finalidad para la que se traten los datos obtenidos</h4>
				<dl>
					<div>
						<dt>Cookies técnicas</dt>: <dd>son estrictamente necesarias para el funcionamiento de la página web. Hacen posible la identificación y el acceso de los usuarios al sistema, controlan el tráfico desde el servidor web a múltiples usuarios a la vez, etc.</dd>
					</div>
					<div>
						<dt>Cookies de personalización</dt>: <dd>son aquéllas que permiten al usuario acceder al servicio configurando aspectos como el idioma o su configuración regional preferente.</dd>
					</div>
					<div>
						<dt>Cookies de análisis y de rendimiento</dt>: <dd>miden el número de visitas, los criterios de búsquedas dentro de la web, cómo navegan los usuarios en determinados apartados, etc.</dd>
					</div>
					<div>
						<dt>Cookies publicitarias</dt>: <dd>permiten la gestión, de la forma más eficaz posible, de los espacios publicitarios de la web.</dd>
					</div>
					<div>
						<dt>Cookies de publicidad comportamental</dt>: <dd>permiten al titular de la web desarrollar perfiles específicos para mostrar publicidad en función de la información sobre el comportamiento que los usuarios tienen en su web.
						</dd>
					</div>
					<div>
						<dt>Cookies de redes sociales externas</dt>: <dd>Son aquéllas que se utilizan para que los visitantes puedan interactuar con el contenido de diferentes plataformas sociales (Facebook, YouTube, Twitter, LinkedIn, Google+, etc.) y que se generan únicamente para los usuarios de dichas redes.
 						</dd>
					</div>
				</dl>
			</section>
