		<div class="vertical-padding-1 horizontal-delimiter">
			<h3>¿CÓMO EMPEZÓ TODO?</h3>
			<br><br>
			<p>
				De pequeña no fui una amante de los gatos y mi familia tampoco, los considerábamos poco afectivos y «aprovechados». Sentía pasión y fui compañera  de muchos otros animales, pero con los gatos no sentía esa conexión y, además, les tenía alergia.
			</p>
			<br><br>
			<p>
				Las cosas empezaron a cambiar casi terminando la secundaria. Hubo que dormir a mi perra por una enfermedad y, a partir de ese momento, los gatos empezaron a cruzarse en mi camino; una se metió en mi jardín y otro, muy pequeño, se me cruzó mientras paseaba con mi madre, al que me empeñé en encontrarle un hogar.
			</p>
			<br><br>
			<p>
				Fueron estos casos los que me dieron a conocer a un veterinario de confianza con el que inicié mis primeras prácticas antes de acudir a la universidad. Allí, empecé a cuestionarme cómo convivimos con los animales, cómo los tratamos, qué tanto sabemos sobre ellos y si, realmente, hacemos todo lo que está a nuestro alcance para ayudarles.
			</p>
			<br><br>
			<h4>¿Realmente sabíamos las necesidades de estos animales una vez conviviendo con humanos?</h4>
			<p>
				Acudí a la universidad y me gradué en Veterinaria de pequeños animales, sabiendo que mi miedo y alergia por los gatos persistía. Además, los gatos no eran evaluados como especie sino más bien como «perros pequeños». Eran un misterio y nada fáciles de tratar en el consultorio.
			</p>
			<br><br>
			<p>
				Decidida a enfrentar mis miedos, hice las prácticas con M. V. Alejandro Paludi, especialista en gatos de Buenos Aires. Allí aprendí, a prueba y error, cómo se comunica un gato y cómo ayudarle sin morir en el intento. Su instinto depredador los hace difíciles de diagnosticar y estudiarles es un desafío constante, pero alimenta las mentes curiosas como la mía. De pronto, los gatos eran mi devoción.
			</p>
			<br><br>
			<p>
				Asistí a diversas clases, charlas y seminarios de especialistas, como Susan Little. Y no conforme con ello, fui invitada a Texas A&M University para realizar prácticas junto al especialista en gatos, John August. Al volver a Argentina, me diplomé en Medicina Felina.
			</p>
			<br><br>
			<h3>MI PROPÓSITO</h3>
			<br><br>
			<p>
				Al poco tiempo, me mudé a España, donde la medicina felina estaba en auge pero los problemas de comportamiento, de diagnóstico, tratamiento y el abandono seguían siendo un problema.
			</p>
			<br><br>
			<p>
				Consciente de que había que hacer algo, nació Felvet, con el propósito de mejorar el vínculo humano-gato y dar solución a los desafíos que los gatos presentan como animal de compañía. Comencé, y continúo, trabajando para Abriga, que gestiona La Gatoteca, un café con gatos rescatados a los que se les busca un nuevo hogar definitivo. Este lugar puso a pruebas mis conocimientos y mis métodos, adaptando a muchos gatos a la vez, concienciando adoptantes y creando cursos que permitieran desvelar el misterio de los gatos.
			</p>
			<br><br>
			<p>
				Seis años después, Abriga es uno de los centros con menos porcentaje de devolución y un centro referente sobre gatos, gracias  a la colaboración Felvet-Abriga. Tanto a nivel de protección animal como a nivel particular quiero que puedas disfrutar al 100% de la compañía tu gato.
			</p>
